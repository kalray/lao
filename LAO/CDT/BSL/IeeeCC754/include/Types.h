/***************************************************************************
* In this file, the basic integer datatypes used in IeeeCC754 are declared
* via typedefs. Necessary because on i386 and solaris architectures, longs
* usually are 32 bits long whereas on x86_64, longs have 64 bits.
* Beno√Æt Dupont de Dinechin 2017: typedef(s) based on <stdint.h>.
************************************************************************** */
#ifndef _TYPES_H
#define _TYPES_H

#include <limits.h>
#include <stdint.h>

typedef int8_t char8;
typedef uint8_t uchar8;

typedef int32_t long32;
typedef uint32_t ulong32;

typedef int64_t long64;
typedef uint64_t ulong64;


#endif // _TYPES_H
